// Menu Category Filtering
const menuButtons = document.querySelectorAll('.menu-btn');
const menuCategories = document.querySelectorAll('.menu-category');

menuButtons.forEach(button => {
    button.addEventListener('click', () => {
        // Remove active class from all buttons
        menuButtons.forEach(btn => btn.classList.remove('active'));
        // Add active class to clicked button
        button.classList.add('active');
        
        const category = button.getAttribute('data-category');
        
        // Hide all categories
        menuCategories.forEach(cat => cat.classList.remove('active'));
        // Show selected category
        const targetCategory = document.getElementById(category);
        if (targetCategory) {
            targetCategory.classList.add('active');
        }
    });
});

// Reservation form handling
const reservationForm = document.querySelector('.reservation-form');
if (reservationForm) {
    reservationForm.addEventListener('submit', function(e) {
        e.preventDefault();
        
        // Get form data
        const name = this.querySelector('input[type="text"]').value;
        const email = this.querySelector('input[type="email"]').value;
        const phone = this.querySelector('input[type="tel"]').value;
        const guests = this.querySelector('select').value;
        const date = this.querySelector('input[type="date"]').value;
        const time = this.querySelectorAll('select')[1].value;
        const requests = this.querySelector('textarea').value;
        
        // Simple validation
        if (!name || !email || !phone || !guests || !date || !time) {
            showNotification('Please fill in all required fields', 'error');
            return;
        }
        
        // Check if date is in the future
        const selectedDate = new Date(date);
        const today = new Date();
        today.setHours(0, 0, 0, 0);
        
        if (selectedDate < today) {
            showNotification('Please select a future date', 'error');
            return;
        }
        
        // Simulate form submission
        const submitBtn = this.querySelector('button[type="submit"]');
        const originalText = submitBtn.textContent;
        submitBtn.textContent = 'Processing...';
        submitBtn.disabled = true;
        
        setTimeout(() => {
            showNotification('Reservation confirmed! We\'ll call you to confirm details.', 'success');
            this.reset();
            submitBtn.textContent = originalText;
            submitBtn.disabled = false;
        }, 2000);
    });
}

